# CRIE UM PROGRAMA QUE LEIA UMA LISTA DE
# NUMEROS E EXIBA O MAIOR E O MENOR VALOR DA LISTA.

lista_numeros = ['2', '3', '5',
         '7', '1', '8']

valor_maior = max(lista_numeros)
valor_menor = min(lista_numeros)

print(f"O maior valor da lista é {valor_maior}")
print(f"O menor valor da lista é {valor_menor}")
